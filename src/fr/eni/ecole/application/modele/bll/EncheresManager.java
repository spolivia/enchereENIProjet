package fr.eni.ecole.application.modele.bll;

public class EncheresManager {
//	
//	Vendre un article
//	En tant qu’utilisateur, je peux vendre un article sur la plateforme ENI-Enchères. Pour cela je donne les informations sur l’article vendu :
//		nom, description et catégorie j’indique un prix de départ ( en points ), une date et une heure d’ouverture de l’enchère,
//		une date et une heure de fin d’enchères et les modalités du retrait :  adresse (par défaut celle du vendeur).
//		
//	Lister les enchères en mode déconnecté
//	En tant qu’utilisateur non connecté, je peux lister les enchères en cours. Je peux filtrer ma recherche par catégorie, et par nom d’article
//	(l’article est affiché si il contient le critère saisi) – Pour consulter le détail des enchères, l’utilisateur doit se connecter.
//
//	Lister les enchères en mode connecté
//	En tant qu’utilisateur connecté, je peux lister les enchères en cours, les enchères auxquelles je participe,
//	c’est à dire celles sur lesquelles j’ai fait au moins une offre, et mes enchères gagnées.
//	Je peux aussi sélectionner mes ventes, non commencées, en-cours ou terminées
//
//	Faire une enchère
//	En tant qu’utilisateur, je peux faire une enchère sur un article si je propose un prix (en points) supérieur au tarif actuel 
//	et si mon compte de points ne devient pas négatif. Si l’enchère est possible, mon crédit de points est débité du montant proposé.
//	Le meilleur enchérisseur précédent si il existe est re-crédité de son offre.
//	
//	Remporter une vente
//	En tant qu’enchérisseur, je deviens acquéreur si au terme de l’enchère j’ai proposé l’enchère la plus haute.
//
//	Afficher le détail d’une enchère
//	En tant qu’utilisateur, je peux afficher le détail d’une enchère. Les informations sur l’article vendu sont affichés
//	( nom, description, meilleure offre, mise à prix, début et fin de l’enchère, adresse de retrait, vendeur.
//			En fonction de l’état de la vente, et du rôle de l’utilisateur (vendeur ou acheteur), l’utilisateur peux seulement consulter les information,
//			enchérir, ou modifier la vente (si il est le vendeur et que l’enchère n’a pas débuté).
//	
//	Facultatif :
//		
//		Modifier une vente
//		En tant que vendeur, je peux modifier les informations liées à ma vente tant que la date de début d’enchère n’est pas arrivée.
//
//		Annuler une vente
//		En tant que vendeur d’un article, je peux annuler cette vente tant que la date de début d’enchère n’est pas arrivée
//
//		Photo pour la vente
//		en tant que vendeur, je peux uploader une photo de l’article à vendre. Celle ci sera visible sur le détail de la vente.
//
//		Pagination
//		Sur la page permettant de lister les enchères suivant des critères de recherche, j’affiche un nombre d’enchères maximum ( 6)
//		et  j’accède  aux autres pages résultat par l’intermédiaire de liens numérotés.
//
//		Voir les enchérisseurs
//		En tant que vendeur, je peux voir la liste des enchérisseurs de mes ventes.
//		Les enchérisseurs sont triés en fonction du montant de leur dernière enchère par ordre décroissant.

}
